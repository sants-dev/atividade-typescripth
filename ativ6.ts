// Definindo o número do mês
let mes: number = 1;

// Estrutura switch para verificar qual mês corresponde ao número
switch (mes) {
    case 1:
        console.log("Janeiro");   // Se mes = 1
        break;
    case 2:
        console.log("Fevereiro"); // Se mes = 2
        break;
    case 3:
        console.log("Março");     // Se mes = 3
        break;
    case 4:
        console.log("Abril");     // Se mes = 4
        break;
    case 5:
        console.log("Maio");      // Se mes = 5
        break;
    case 6:
        console.log("Junho");     // Se mes = 6
        break;
    case 7:
        console.log("Julho");     // Se mes = 7
        break;
    case 8:
        console.log("Agosto");    // Se mes = 8
        break;
    case 9:
        console.log("Setembro");  // Se mes = 9
        break;
    case 10:
        console.log("Outubro");   // Se mes = 10
        break;
    case 11:
        console.log("Novembro");  // Se mes = 11
        break;
    case 12:
        console.log("Dezembro");  // Se mes = 12
        break;
    default:
        console.log("Mês inválido"); // Caso o número não seja entre 1 e 12
}
